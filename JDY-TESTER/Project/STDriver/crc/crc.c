uint16  crcm(uint8 *buffer,int16 length,int16 fig)
{
   uint16 crcword;
   int16 i;

   crcword = 0xffff;
//   if(length > 255) return FALSE; 
   for(i = 0 ;i < length ;i++)
	   crcupdate (buffer[i] ,&crcword ,crctab);
   crcword ^= 0xffff;

   if(fig == CRC_SEND) 
   {
	   buffer[i++] = crcword >> 8;
	   buffer[i] = crcword & 0xff;
	   return crcword;
   }
   else
   {
	   if (crcword == 0xe2f0)
	       return TRUE;
	   else
	       return FALSE;
   }
}

void crcupdate(uint8 datat,uint16 *accum,const uint16 *crctab)
{
   static int16 comb_val;
   comb_val = (*accum >> 8) ^ datat;
   *accum = (*accum << 8) ^ crctab[comb_val];
}

uint16 CalCS(uint8* buf, uint16 lenth)
{
	uint16 i, cs = 0;
	for(i = 0;i < lenth; i++)
	{
		cs += *(buf+i);
	}
	return cs;
}

uint16 ModbusCRC(uint8 *data, uint16 len)
{
	int16 i, j;
	uint16 crc=0xFFFF, flag=0;

	for (i = 0; i < len; i++)
	{
		crc ^= data[i];
		for (j = 0; j < 8; j++)
		{
			flag = crc & 0x0001;
			crc >>= 1;
			if (flag)
			{
				crc ^= 0xA001;
			}
		}
	}
	return crc;
}


