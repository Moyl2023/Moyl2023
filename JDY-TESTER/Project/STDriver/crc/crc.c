unsigned short CalcCRC1021(unsigned char *pu8Buf, unsigned int u32Len)//CRC校验
{
	unsigned int		i;
	unsigned char		u8Data;
	unsigned short		u16CRCData;


	u16CRCData = 0;
	for(i = 2; i< u32Len; i ++)
	{
		u8Data = ((u16CRCData >> 8) ^ pu8Buf[i]) & 0x0FF;
		u16CRCData = pu16CRC1021Table[u8Data] ^ (u16CRCData << 8);
	}

	return u16CRCData;
}

/***************************************************************************
** BOOL RxdCrc( U8* p )
** 功能: 校验接收到的数组
** 参数: u8 *p 数组指针
** 输出:
** 描述:监控器和BAT30M使用
****************************************************************************/
uint16 RxdCrc(uint8* p,uint8 length)
{
          uint8 i;
          uint8 locat;
          union
          {
                    uint16 iWord;
                    struct
                    {
				uint8 cLow;
                              uint8 cHigh;
                    }
                    iData;
          }
          CrcTemp;
          CrcTemp.iWord = 0xffff;
          for( i=0; i<length; i++ )
          {
                    locat = CrcTemp.iData.cHigh^p[i];
                    CrcTemp.iData.cHigh = CrcTemp.iData.cLow;
                    CrcTemp.iData.cLow = 0;
                    CrcTemp.iWord ^= crctab[locat];
          }
          CrcTemp.iWord ^= 0xffff;
          if( CrcTemp.iWord == 0xf02e )
          {
                    return CrcTemp.iWord;
          } 
		  return CrcTemp.iWord;       
}

/***************************************************************************
** void TxdCrc( U8* p)
** 功能: 校验发送的数组
** 参数: u8 *p 数组指针
** 输出:
** 描述:监控器和BAT30M使用
****************************************************************************/
uint16  TxdCrc(uint8* p,uint8 length)
{
          uint8 i;
          uint8 count;
          uint8 locat;
          union
          {
                    uint16 iWord;
                    struct
                    {
                              uint8 cLow;
                              uint8 cHigh;
                    }
                    iData;
          }
          CrcTemp;
          count = p[0]-2;
          CrcTemp.iWord = 0xffff;
          for( i=0; i<count; i++ )
          {
                    locat = CrcTemp.iData.cHigh^p[i];
                    CrcTemp.iData.cHigh = CrcTemp.iData.cLow;
                    CrcTemp.iData.cLow = 0;
                    CrcTemp.iWord = CrcTemp.iWord^crctab[locat];
          }
          CrcTemp.iWord ^= 0xffff;
          p[i++] = CrcTemp.iData.cHigh;
          p[i] = CrcTemp.iData.cLow;

		  return CrcTemp.iWord;
}

